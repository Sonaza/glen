#include <glen/System/Time.hpp>

namespace glen
{

/////////////////////////////////////////////////////////////
Time::Time(const int64 microseconds) :
	m_ticks(microseconds)
{}

/////////////////////////////////////////////////////////////
float Time::asSeconds() const
{
	return m_ticks / 1000000.f;
}

/////////////////////////////////////////////////////////////
int64 Time::asMilliseconds() const
{
	return m_ticks / 1000;
}

/////////////////////////////////////////////////////////////
int64 Time::asMicroseconds() const
{
	return m_ticks;
}

/////////////////////////////////////////////////////////////
Time seconds(float amount)
{
	return Time(static_cast<int64>(amount * 1000000));
}

/////////////////////////////////////////////////////////////
Time milliseconds(int64 amount)
{
	return Time(amount * 1000);
}

/////////////////////////////////////////////////////////////
Time microseconds(int64 amount)
{
	return Time(amount);
}

}